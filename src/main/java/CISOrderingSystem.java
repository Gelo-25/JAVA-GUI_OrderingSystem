 public void ItemCost()
 {
     double sum = 0;
     for (int i = 0; i < jTable1.getRowCount();i++) 
    {
         sum = sum + Double.parseDouble(jTable1.getValueAt(i,2).toString());
     }
     jTF1.setText(Double.toString(sum));
     double cTotal = Double.parseDouble(jTF1.getText());
     double cTax = (cTotal * 0.12);
     //===PWD/Senior Discounts=====
     double initialTotal = cTotal - cTax;   //total - VAT  
     
    double discount = 0;
    if (pwd.isSelected()) {
        discount = 0.05; // 5% discount for PWD
    }
    if (senior.isSelected()) {
        discount = 0.2; // 20% discount for seniors
    }
    
    // Apply discount
    double discountAmount = initialTotal * discount; 
    double discountTotal = initialTotal - discountAmount ;
    
    //Apply the VAT to the discounted total
    double finalTotal = discountTotal + cTax; 
        
     //===TAX=====
     String iTaxTotal = String.format(" %.2f ", cTax);
     jTF2.setText(iTaxTotal);
     //=====SubTOtal======
     String iSubTotal = String.format (" %.2f ", cTotal - cTax);
     jTF1.setText(iSubTotal);
     //======Total======
     String ifinalTotal = String.format(" %.2f", finalTotal );
        jTF3.setText(ifinalTotal);
     
     
    

     
     
     
 }
