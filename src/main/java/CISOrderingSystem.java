    private void jBExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExitActionPerformed
DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
LocalDateTime now = LocalDateTime.now();
String timestamp = dtf.format(now);

Random random = new Random();
int randomNumber = random.nextInt(1000); // Generate a random number between 0 and 999
String uniqueTaxNo =timestamp.replace("/","").replace(" ", "").replace(":", "")+ randomNumber;

String posSerialNo = ReceiptGenerator.generatePOSSerialNo(); //POS Serial No.

String ts = jTF3.getText();
double dts = Double.parseDouble(ts);
String ca = jTF4.getText();
double dca = Double.parseDouble(ca);
String aa = (String) jCBPayment.getSelectedItem();

// Top part of Receipt || Instead of used "setText" we will use "append" because "setText" only allows 1 body of text while "append" adds new text
jR.append(
    "                     Grp10 Shop     \n"+
    "                 Home Appliances   \n"+
    "                                   \n"+
    "    Col. E. De Leon st. Wawa, Brgy. Sto. Niño,\n"+
    "               Parañaque City, 1700"+"\n"+
    "               Ref: 4200060009000"+"\n"+
    "               Tax No: "+ uniqueTaxNo + "\n"+
    "                   +1 234 567 890\n"+
    "               POS Serial No: "+ posSerialNo + "\n"+
    "=======================================\n"+
    "\n"+
            "          Order                           QTY                   Amt"+ "\n"+
    "\n");

        // This Code will Loop through the rows of the table and append the item details to the receipt text area
        // first we set Interger "i" to get row count || i++ is looping additive value from 1 ->this will stop based on how many rows are detected
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String item = jTable1.getValueAt(i, 0).toString();
            String quantity = jTable1.getValueAt(i, 1).toString();
            String total = jTable1.getValueAt(i, 2).toString();
            //This text right here will reflect selected items, quantity, and total on the receipt
            jR.append("          "+item          + "                 " + quantity + "                    ₱" + total + "\n");
        }

        // Final line with the total amount || VAT is 12% of our sales
        String saleIndicator = "0.00";
if (sale.isSelected()) {
    saleIndicator = "Applied";
} else if (sale25.isSelected()) {
    saleIndicator = "Applied";
} else if (sale10.isSelected()) {
    saleIndicator = "Applied";
} else if (sale70.isSelected()) {
    saleIndicator = "Applied";
} else if (flash.isSelected()) {
    saleIndicator = "Applied";
}else if (pwd.isSelected()) {
    saleIndicator = "Applied";
}else if (senior.isSelected()) {
    saleIndicator = "Applied";
}





jR.append(
    "\n"+
    "=======================================\n"+
    "         Payment:                     "+ aa +"\n"+
    "         Total Amount:              ₱"+ (dts-(dts*0.12)) +"\n"+
    "         VAT:                               ₱"+(dts*0.12) +"\n"+
    "         Final Charge:              ₱"+ ts +"\n"+
    "         Discount:                      " + saleIndicator + "\n" +
    "=======================================\n"+
    "         Balance:                       ₱"+ dca +"\n"+
    "         Change:                       ₱"+ (dca-dts) +"\n"+
    "                                          \n"+
    "    Thank you for coming!!!\n"+
    "    Visit our website www.homeappliances.com \n"+
    "    Time and Date of Transaction: \n"+
    "                     "+ dtf.format(now)
);


    }//GEN-LAST:event_jBExitActionPerformed

