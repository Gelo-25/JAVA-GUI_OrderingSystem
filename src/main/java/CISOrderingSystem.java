    public static int generatePosSerialNumber() {
        Random random = new Random();
        return random.nextInt(9999999);
    }
    private static String generatePOSSerialNo() {
        Random random = new Random();
        StringBuilder posSerialNo = new StringBuilder();

        // Append 2 random digits
        for (int i = 0; i < 2; i++) {
            posSerialNo.append(random.nextInt(10));
        }

        // Append 3 random uppercase letters
        for (int i = 0; i < 3; i++) {
            posSerialNo.append((char) (random.nextInt(26) + 'A'));
        }

        // Append 5 random digits
        for (int i = 0; i < 5; i++) {
            posSerialNo.append(random.nextInt(10));
        }

        return posSerialNo.toString();
    }
